public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Arithmetic Operators!");

        // Addition
        int x = 1 + 2;
        System.out.println(x);

        // Subtraction
        int y = 1 - 2;
        System.out.println(y);

        // Multiplication
        int z = 1 * 2;
        System.out.println(z);

        // Division
        int a = 1 / 2;
        System.out.println(a);

        // Modulus
        int b = 10 % 3;
        System.out.println(b);

    }
}
