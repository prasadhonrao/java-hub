public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Unary Operators!");

        int x = 1;
        x++;
        System.out.println(x);

        int y = 1;
        y--;
        System.out.println(y);

        int z = 1;
        z = -z;
        System.out.println(z);

        boolean a = true;
        System.out.println(!a);
        
    }
}
