public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Relational Operators!");

        // Greater than
        System.out.println(1 > 2); // false

        // Greater than or equal to
        System.out.println(1 >= 2); // false

        // Less than
        System.out.println(1 < 2); // true

        // Less than or equal to
        System.out.println(1 <= 2); // true

        // Equal to
        System.out.println(1 == 2); // false

        // Not equal to
        System.out.println(1 != 2); // true

    }
}
