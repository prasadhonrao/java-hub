public class App {
    public static void main(String[] args) throws Exception {
        System.out.println("Boolean Data Type!");

        // Boolean data type has only two possible values: true and false
        // Use this data type for simple flags that track true/false conditions

        Boolean myTrueBooleanValue = true;
        Boolean myFalseBooleanValue = false;

        System.out.println("Boolean Value = " + myTrueBooleanValue);
        System.out.println("Boolean Value = " + myFalseBooleanValue);

        // Boolean assignment
        boolean isCustomerOverTwentyOne = true;
        System.out.println("Boolean Value = " + isCustomerOverTwentyOne);
    }
}
